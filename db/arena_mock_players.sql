-- ── 3. ตรวจสอบ
select
  row_number() over (order by l.score desc) as rank,
  p.username,
  p.title,
  l.score,
  l.ending,
  case
    when l.score >= 1000 then '👑 แชมเปี้ยน'
    when l.score >= 600  then '🥇 โกลด์'
    when l.score >= 300  then '🥈 ซิลเวอร์'
    else                      '🥉 บรอนซ์'
  end as tier
from players p
join leaderboard l on l.player_id = p.id
where p.id::text like '99999999-%'
order by l.score desc;
